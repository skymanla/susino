					5. 개인정보 제공 및 공유<br />
					6. 수집한 개인정보의 취급위탁 <br />
					7. 이용자 및 법정대리인의 권리와 그 행사방법<br />
					8. 동의철회 / 회원탈퇴 방법<br />
					9. 개인정보 자동 수집 장치의 설치/운영 및 그 거부에 관한 사항<br />
