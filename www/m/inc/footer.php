					스시노백쉐프(이하 "회사" 라 함) 귀하의 개인정보보호를 매우 중요시하며, 『개인정보보호법』을 준수하고 있습니다. 회사는 개인정보처리방침을 통하여 귀하께서 제공하시는 개인정보가 어떠한 용도와 방식으로 이용되고 있으며 개인정보보호를 위해 어떠한 조치가 취해지고 있는지 알려드립니다.<br />
					이 개인정보처리방침의 순서는 다음과 같습니다.<br />
					<br />
					1. 수집하는 개인정보의 항목 및 수집방법<br />
					2. 개인정보의 수집 및 이용목적<br />
					3. 개인정보의 보유 및 이용기간<br />
