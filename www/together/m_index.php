function write_ok()
{
	var f = document.forms.myform;

	if(f.name.value == "")
	{
		alert('이름을 입력하세요.');
		f.name.focus();
		return false;
	}

	if(f.hp.value == '')
	{
		alert('연락처를 입력하세요.');
		f.hp.focus();
		return false;
	}

	var regExp = /^01([0|1|6|7|8|9]?)-?([0-9]{3,4})-?([0-9]{4})$/;
	if( !f.hp.value )
	{
	   alert("연락처를 입력해주세요");
	   f.hp.focus();
	   return false;
	}
	else
	{
		if(!regExp.test(f.hp.value))
		{
			alert("잘못된 휴대폰 번호입니다. 숫자, - 를 포함한 숫자만 입력하세요");
			f.hp.focus();
			return false;
	   }
	}

	var regExp2 = /^[0-9a-zA-Z]([-_\.]?[0-9a-zA-Z])*@[0-9a-zA-Z]([-_\.]?[0-9a-zA-Z])*\.[a-zA-Z]{2,3}$/i;
	if( !f.email.value )
	{
	   alert("이메일주소를 입력해주세요");
	   f.email.focus();
	   return false;
	}
	else
	{
		if(!regExp2.test(f.email.value))
		{
			alert("이메일 주소가 유효하지 않습니다");
			f.email.focus();
			return false;
	   }
	}

	if(f.content.value == '')
	{
		alert('문의내용을 입력하세요.');
		f.content.focus();
		return false;
	}

	var w_rull_check = $('#chk_1').prop('checked');

	if(!w_rull_check)
	{
		alert('개인정보 활용동의에 체크해주세요.');
		$('.chk_1').focus();
		return false;
	}

	f.action = '../../lib/write_ok.php';
	f.submit();
}
