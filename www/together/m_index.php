	function mMenuOpenClose(){
		var $btnOpenMenu = $('.bt_menu'),
			 $btnCloseMenu = $('.bt_close');

		$btnOpenMenu.on('click',function(){
			$(this).closest('#wrap').find('.menu_wrap').addClass('active');
			$(this).next('.mask').addClass('on');
		});
		$btnCloseMenu.on('click',function(){
			$(this).closest('.menu_wrap').removeClass('active');
			$(this).closest('#wrap').find('.mask').removeClass('on');
		})
	}
	function animate(){
		var $navi = $('#navi')
		$navi.find('li button').on('click',function(e){
			e.preventDefault();
			var idx = $(this).closest('li').index();
			var _targetHei = $(this).closest('#wrap').find('#content > div').eq(idx).offset().top;
			$('body,html').stop().animate({scrollTop: _targetHei},300);
			$(this).closest('.menu_wrap').removeClass('active');
			$(this).closest('#wrap').find('.mask').removeClass('on');
		});
	}
	function active() {
		var $btnBox = $('#box3 .con_wrap .con_box');
		var $btnTab = $('#box3 .con_wrap .con_box > li button');
		$btnTab.on('click',function(e){
			var $_This = $(this);
			var $_ThisPar = $(this).parent();
			var $_ThisNext = $(this).next();
			setTimeout(function(){
				var offTop = $_This.offset().top;
				$('body, html').stop().animate({scrollTop:offTop}, 300);
			}, 100);

			e.preventDefault();

			if($_This.next().is(':visible')){
				$_ThisPar.removeClass('active');
				$_ThisNext.stop().slideUp(100);
			} else {
				$_ThisPar.addClass('active').siblings().removeClass('active').find('.img_wrap').slideUp(100);
				$_ThisNext.stop().slideDown(100);
			}

		});

